package CodeForces;

import java.util.Scanner;

public class Filling_Diamonds {
    public static void main(String[] args) {
        new Filling_Diamonds().run();
    }

    private void run() {
        Scanner sc = new Scanner(System.in);
        int t = sc.nextInt();

        while(t-->0) {
            int n = sc.nextInt();
            System.out.println(n);
        }
    }
}
